// Get all unique location names for autocomplete
export const locationNames = priceList.map(e => e.name);

// Find price by location name (fuzzy search)
export function findPrice(query: string): PriceEntry | null {
  const q = query.toLowerCase().trim();
  if (!q) return null;
  // Exact match
  const exact = priceList.find(e => e.name.toLowerCase() === q);
  if (exact) return exact;
  // Starts with
  const starts = priceList.find(e => e.name.toLowerCase().startsWith(q));
  if (starts) return starts;
  // Contains
  const contains = priceList.find(e => e.name.toLowerCase().includes(q));
  if (contains) return contains;
  // Reverse: query contains entry name
  const reverse = priceList.find(e => q.includes(e.name.toLowerCase()));
  if (reverse) return reverse;
  return null;
}

// Search locations for autocomplete
export function searchLocations(query: string): PriceEntry[] {
  if (!query || query.length < 1) return [];
  const q = query.toLowerCase().trim();
  return priceList
    .filter(e => e.name.toLowerCase().includes(q))
    .slice(0, 15);
}

